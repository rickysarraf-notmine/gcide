int get_rand( int ll, int ul )
{
   double r = rnd[randpt++];
   int    val;

   if (randpt >= MAXRND)
      err_internal( __FUNCTION__,
		    "Ran out of random numbers (only %d available)\n",
		    MAXRND );

   val = floor( ll + r * (ul - ll) );
   if (val > ul)
      err_internal( __FUNCTION__, "%d > %d\n", val, ul );
   if (val < ll)
      err_internal( __FUNCTION__, "%d < %d\n", val, ll );
   return val;
}
